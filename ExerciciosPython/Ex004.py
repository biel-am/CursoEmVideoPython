info = input('Digite algo: ')
print(f'AlphaNum = {info.isalnum()}')
print(f'Alpha = {info.isalpha()}')
print(f'Title = {info.istitle()}')
print(f'Upper = {info.isupper()}')
print(f'Lower = {info.islower()}')
print(f'Decimal = {info.isdecimal()}')
print(f'Space = {info.isspace()}')
print(f'Ascii = {info.isascii()}')
print(f'Digit = {info.isdigit()}')
print(f'Identifier = {info.isidentifier()}')
print(f'Printable = {info.isprintable()}')
